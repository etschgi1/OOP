#include "Treasure.hpp"

//----------------------------------------------------------------------------------------------------------------------
Treasure::Treasure(string name, string treasure_id) : name_{std::move(name)}, treasure_id_{std::move(treasure_id)} {}

//----------------------------------------------------------------------------------------------------------------------
string Treasure::getName() const
{
  return name_;
}

//----------------------------------------------------------------------------------------------------------------------
string Treasure::getTreasureId() const
{
  return treasure_id_;
}

//----------------------------------------------------------------------------------------------------------------------
bool Treasure::isFound() const
{
  return found_;
}

//----------------------------------------------------------------------------------------------------------------------
void Treasure::setFound(bool found)
{
  found_ = found;
}
