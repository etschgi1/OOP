#include "Player.hpp"

//------------------------------------------------------------------------------------------------------------------
Player::Player(char color) : color_{color}
{
  number_found_treasures_ = 0;
}

//------------------------------------------------------------------------------------------------------------------
void Player::found()
{
  number_found_treasures_++;
}

//------------------------------------------------------------------------------------------------------------------
stack<shared_ptr<Treasure>>& Player::getStack()
{
  return covered_stack_;
}

//------------------------------------------------------------------------------------------------------------------
char Player::getColor() const
{
  return color_;
}

//------------------------------------------------------------------------------------------------------------------
void Player::setDone(bool done)
{
  done_ = done;
}

//------------------------------------------------------------------------------------------------------------------
bool Player::getDone() const
{
  return done_;
}
